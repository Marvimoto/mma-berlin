        <h2 id="bjj" class="anchor">Brazilian Jiu Jitsu</h2>
        <hr>
        <p align="justify">
            Jiu-Jitsu = Sanfte Kunst<br>

            Das klassische Jiu-Jitsu entstand in Japan zur Zeit der Samurai und beinhaltete alle Elemente des
            waffenlosen Kampfes im Stand und auch am Boden. Die Bezeichnung "Sanfte Kunst" ist mehr auf das Prinzip
            zurückzuführen, die eigene Kraft intelligent und mit Technik zu nutzen, anstatt Kraft gegen Kraft wirken zu
            lassen. Brazilian Jiu-Jitsu ist eine Stilrichrichtung, die sich aus dem klassischen Jiu-Jitsu entwickelt
            hat. Der Schwerpunkt liegt hier im Bodenkampf (Kontrolle, Würgegriffe, Hebel). Meist wird Brazilian
            Jiu-Jitsu mit Gi (weisser Anzug wie beim Judo) trainiert und auch bei uns tragen manche, die aus dem
            Jiu-Jitsu, Judo oder ähnlichem kommen, zumindest manchmal einen Anzug. Vor- und Nachteil zugleich ist
            hierbei, dass man mit Anzug den Gegner besser kontrollieren kann, aber genauso umgekehrt auch selbst besser
            kontrolliert werden kann.
            <br><br>
            · Literatur: <a href="http://www.amazon.de/exec/obidos/ASIN/1931229082/ref=pd_sim_dp_2/028-7120405-4626153"
                            target="blank">Brazilian Jiu-Jitsu - Theory and Technique</a><br>
            · Brazilian Jiu-Jitsu & MMA-Legenden: <a href="http://www.rickson.com/" target="blank">Rickson Gracie</a>
            und <a href="http://www.roycegracie.tv/" target="blank">Royce Gracie</a><br>
