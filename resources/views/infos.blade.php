        <h2 id="judo" class="anchor">Judo</h2>
        <hr>
        <p align="justify">
            Der sanfte Weg<br>

            Judo ist von vielen im Freefight-Bereich sehr unterschätzt. Abgesehen von sehr wirkungsvollen Techniken wie
            Armhebeln und Würgegriffen ist ein grosser Vorteil im Judo, dass man im Randori / Sparring mit voller Kraft
            und Schnelligkeit kämpfen kann, ohne sich zu verletzen. (Will man z.B. beim Thaiboxsparring "realistisch"
            mit Knie zum Kopf etc. kämpfen, gestaltet sich das nicht so einfach. Auch trotz entsprechender
            Schutzausrüstung muss man wesentlich vorsichtiger sein...).
            <br><br>
            · Judoka: <a href="http://www.hidehiko.jp/" target="_blank">Hidehiko Yoshida</a><br>
