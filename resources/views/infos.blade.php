        <h2 id="sambo" class="anchor">Sambo</h2>
        <hr>
        <p align="justify">
            Samosaschtschita Bes Orushija
            (Selbstschutz ohne Waffen)<br>

            Sambo ist ein russischer Kampfsport. Es hat sich aus verschiedenen Ringkampfstilen, Judo und später auch aus
            anderen Elementen des Militärtrainings entwickelt. Die Militär-/ Combatsambo-Variante ist dabei ein sehr
            umfassendes System. Ausser den üblichen Techniken im Stand werden hier verschiedenste Würfe, sämtliche Arm-,
            Schulter-, Fuss-, Kniehebel und Würgegriffe trainiert (ähnlich dem Jiu-Jitsu oder Allkampf). Bekannt ist
            Sambo vielen durch seine starken und sehr vielfältigen Fuss- / Kniehebel und anderen Beintechniken.
            <br><br>
            · Literatur: <a href="http://www.weinmann-verlag.de/sambo.htm" target="_blank">SAMBO ... der kraftvolle
                russische Kampfsport</a><br>
